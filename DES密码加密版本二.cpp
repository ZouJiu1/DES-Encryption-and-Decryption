class initi
{
private:
    int i,j,c,b,bin[200][8][8],e,t,h;
    char a[1600],aa[200][8];
public:
    int f[12800],count=0;
    int*fe(void)
    {
        cout<<"输入待加密的字符（不超过1600个，之内个数没有限制）：";
        cin>>a;
        for(i=0;a[i]!='\0';i++)//转换为ASCII码值
        {
            count++;
        }
        h=(count-1)/8;
        for(i=0;i<=h*8;)//对字符分段，每段8个字符
        {
            for(j=0;j<8;j++)
            {
                aa[i/8][j]=a[i+j];
            }
            i=i+8;
        }
//转换为比特流
        for(t=0;t<=h;t++)
        {
            for(j=0;j<8;j++)
            {
                e=(int)aa[t][j];
                for(i=0;i<10;i++)//把ASCII值转换为二进制
                {
                    b=e/2;
                    bin[t][j][i]=e%2;
                    e=b;
                    if(e==0)//化到最后都会从2或1化到0
                    {
                        for(c=i+1;c<8;c++)//补位添0,成为8bit
                        {
                            bin[t][j][c]=0;
                        }
                        cout<<"\n字符："<<aa[t][j]<<"转换为8bit是：";
                        for(i=7;i>=0;i--)
                        {
                            cout<<bin[t][j][i];
                        }
                        break;
                    }
                }
            }
        }
        c=0;
        for(t=0;t<=h;t++)//按照64比特记录到一个数组中
        {
            for(j=0;j<8;j++)
            {
                for(i=7;i>=0;i--)
                {
                    f[c]=bin[t][j][i];
                    c++;
                }
            }
        }
        if(count%8!=0)//字符数不是8的倍数，填充0比特流
        {
            for(i=8*count;i<(h+1)*64;i++)
            {
                f[i]=0;
            }
        }
        return f;
    }
};
