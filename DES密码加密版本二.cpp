class kborn
{
private:
    int i,j,d[56],C[20][28],D[20][28],a,CD[56],c,b,e;
public:
    int ke[48];
    int*ki(int*F,int n)
    {
        for(i=0;i<56;i++)//64bit密钥进行PC-1置换变为56bit
        {
            d[i]=F[PC[i]-1];
        }
        for(i=0;i<28;i++)//划分成两段每段28bit
        {
            C[0][i]=d[i];
        }
        for(i=28;i<56;i++)
        {
            D[0][i-28]=d[i];
        }
        for(i=1;i<=n;i++)//循环移位
        {
            if(i==1||i==2||i==9||i==19)//移一位
            {
                a=C[i-1][0];
                c=D[i-1][0];
                for(j=0;j<27;j++)//左循环移位一位
                {
                    C[i][j]=C[i-1][j+1];
                    D[i][j]=D[i-1][j+1];
                }
                C[i][j]=a;
                D[i][j]=c;
            }
            else//移两位
            {
                a=C[i-1][0];
                b=D[i-1][0];
                c=C[i-1][1];
                e=D[i-1][1];
                for(j=0;j<26;j++)//左循环移位两位
                {
                    C[i][j]=C[i-1][j+2];
                    D[i][j]=D[i-1][j+2];
                }
                C[i][j]=a;
                D[i][j]=b;
                C[i][j+1]=c;
                D[i][j+1]=e;
            }
        }
        for(j=0;j<28;j++)//两个28bit合并为一段56比特
        {
            CD[j]=C[i-1][j];
        }
        for(j=28;j<56;j++)
        {
            CD[j]=D[i-1][j-28];
        }
        for(j=0;j<48;j++)//进行PC-2置换生成密钥
        {
            ke[j]=CD[PCr[j]-1];
        }
        return ke;
    }
};
